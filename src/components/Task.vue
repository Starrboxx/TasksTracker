<template>
    <div @dblclick = "$emit('toggle-reminder',task.id)" :class = "[task.reminder ? 'reminder': '', 'task']">
        <h3>{{task.id}}</h3>
        <h3>{{task.text}}</h3>
        <button @click="onDelete(task.id)">X</button>
        <h3>{{task.day}}</h3>
         
    </div>
   
</template>
<script>
export default{
    name:'Task',
    props:{
        task:Object
    },
     methods:{
        onDelete(id){
            // console.log(id)
            this.$emit('delete-task',id)
        }
    }
    
}
</script>
<style scoped>
    .task{
        background: #f4f4f4;
        margin: 5px;
        padding:10px 20px;
        cursor: pointer;
    }
    .task.reminder{
        border-left: 5px solid green;
    }
    .task h3{
        display: flex;
        align-items: center;
        justify-content: space-between;
    }
</style>